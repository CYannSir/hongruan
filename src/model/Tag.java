package model;

public class Tag {
	private String tid = "";
	private String pid = "";
	private String wid = "";
	private double xleft = 0;
	private double yleft = 0;
	private double xright = 0;
	private double yright = 0;
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getWid() {
		return wid;
	}
	public void setWid(String wid) {
		this.wid = wid;
	}
	public double getXleft() {
		return xleft;
	}
	public void setXleft(double xleft) {
		this.xleft = xleft;
	}
	public double getYleft() {
		return yleft;
	}
	public void setYleft(double yleft) {
		this.yleft = yleft;
	}
	public double getXright() {
		return xright;
	}
	public void setXright(double xright) {
		this.xright = xright;
	}
	public double getYright() {
		return yright;
	}
	public void setYright(double yright) {
		this.yright = yright;
	}
	
}
